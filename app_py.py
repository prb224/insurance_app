# app.py

import streamlit as st
import pandas as pd
import seaborn as sns
import matplotlib.pyplot as plt


st.title('Auto Insurance Premium Prediction')

st.write("""
This is a simple demo app showing insights from the insurance premium prediction project.
""")

# Upload data
uploaded_file = st.file_uploader("insurance_data", type=["csv"])

if uploaded_file is not None:
    data = pd.read_csv(uploaded_file)
    st.write("Data Preview:")
    st.dataframe(data.head())

    st.write("**Premium Amount Distribution**")
    plt.figure(figsize=(8,4))
    sns.histplot(data['Premium_Amount'], kde=True)
    st.pyplot(plt.gcf())

st.write("---")
